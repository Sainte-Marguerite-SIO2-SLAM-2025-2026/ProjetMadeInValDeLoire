        <p>
            Ce grimoire poussiéreux vous introduit à la Salle N°3, dite la Salle des Murmures. Ici, chaque pas réveille
            un souvenir et chaque souffle peut déclencher un mécanisme oublié. La pièce se compose de trois épreuves
            principales: l’Observation, la Mémoire et la Déduction. Rien n’est laissé au hasard; le décor lui‑même est
            un indice. Prenez le temps de détailler les objets: une plume ébréchée près de l’encrier, un verre à moitié
            plein, une montre figée sur une heure impossible… Tous portent la trace du propriétaire, et chacun
            raconte une part de l’histoire du manoir.
        </p>
        <p>
            Épreuve d’Observation: repérez les anomalies visuelles et sonores. Certaines bougies vacillent sans courant
            d’air, des portraits semblent suivre du regard, et des notes de musique manquent sur une partition jaunie.
            Rassemblez ces fragments; ils formeront une première clef. Épreuve de Mémoire: retenez l’ordre des sons,
            la position des objets et les dates disséminées dans la pièce. Tout changement subtil après un clignement
            d’yeux a une signification. Épreuve de Déduction: replacez les éléments dans le bon enchaînement. Quand le
            récit reconstitué concorde, un cliquetis lointain confirme votre progression.
        </p>
        <p>
            Attention: trois erreurs consécutives réveillent l’ombre du gardien. Elle ne parle pas, mais elle efface vos
            repères – la pièce se réorganise, et vous devrez reprendre plus bas. En revanche, si vous avancez avec
            patience, la lumière pâle qui filtre sous la porte s’intensifiera. Alors, seulement, le mécanisme de sortie
            acceptera votre approche. Rappelez‑vous: ce manoir récompense la curiosité méthodique. Écoutez, notez,
            reliez. Le livre vous observe autant que vous le lisez; quand vos hypothèses deviennent justes, ses pages
            semblent moins lourdes, comme prêtes à tourner toutes seules. Bonne chance, détective.
        </p>
